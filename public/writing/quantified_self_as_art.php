                    <p>On January 23rd 2013 I decided to take on a project which has since become the main focus of my life. Last spring I dedicated close to 22 hours a week to this project, and over the summer I spent 36% of my waking hours working on it.</p>

<p>I am speaking about the idea of a &lsquo;Quantified Self&rsquo;&#8202;&mdash;&#8202;the notion that we can use rigourous data collection combined with scientific analysis to arrive at an enlightened understanding of how the &lsquo;self&rsquo; operates. Different people engage in this process in different ways; some use pedometers to track activity, others use heart rate monitors, location sensors, saliva samples, genomic testing, diaries and journals, Facebook posts, foursquare check-ins, and tweets. I have been meticulously tracking exactly where I have spent my time for the past two years.</p>

<p>The purpose of self quantification is very simple. It is a quest to break free from the divinely imposed limits of physicality that are placed on our bodies and minds. The contradiction that arises when we are forced to assign value to our fundamentally feeble physical bodies. Our eyes are trapped between 400-700nm wavelength light, our skin is safe only in the tens of degrees centigrade, our ears collapse when we raise the amplitude, and our movement was once limited to at most a few metres per second. Physics is aesthetically beautiful precisely because it challenges us to go beyond ourselves&#8202;&mdash;&#8202;to consider side-by-side both the gargantuan and the miniscule. Coming to terms with our own physicality is what Nietzsche was doing in his quest for the Uberman, it is the ultimate conclusion of Kant&rsquo;s investigation into the world &ldquo;as it is&rdquo;. The genius of human evolution has been in its use of tools to go beyond what the bones and sinews of the human carcass can do alone. We use knives because we don&rsquo;t have any sharp metal body parts. We ride bicycles so we can travel faster than we can run. We engineer cranes because we can&rsquo;t lift things hundreds of feet into in the air unassisted. We track time because our memories are not quite as precise as we would like them to be.</p>

<p>David Hume taught us that when you throw a pair dice you cannot know how they will land. Unless of course you combine a robust physical model with the precise velocity, trajectory and angular momentum of the die as it left your hand. He showed us that the assumed &ldquo;randomness&rdquo; in the throw of dice, is actually just a feature of information scarcity. I wonder therefore, if people are like dice. We seem to be able to &lsquo;decide&rsquo; how hard to work, how focused to be, how passionate, involved and invested we are in our lives, but perhaps there is a hidden hand at play that&rsquo;s actually causing us to land as we do. Perhaps we are far more influenced by our surroundings than we allow ourselves to believe, and a robust, scientific data collecting process would make this immediately clear to us. We would be less in the dark about what it takes to live truly fulfilled, happy lives. We would stop worshipping sun gods, stop performing rain dances, and stop butchering our bodies and souls in sacrifice to the Gods of success. Instead, we would have a rigourous understanding of how to shape our lives. To me this is powerful.</p>

<p>To call myself an artist seems presumptuous. I draw, I paint, I occasionally sculpt, and I perform poetry&#8202;&mdash;&#8202;these are traditionally considered &lsquo;art forms&rsquo;. However the more I ponder, the more I find myself subscribing to the Nietzchean proclamation that existence itself is justified as a purely aesthetic phenomenon. That the act of living is a work of art. Every decision made is a pencil mark, a smudge, a dab of colour on canvas.</p>

<p>In a sense, I see the quantified self movement as an investigation into the art of living. A hark back to the Platonic question of how one should go about living the perfect life. In this instance however, we are not restricted to the oratory tools of philosophy but can call to our assistance the vast quantitative arsenal that physics and the mathematical sciences have been built upon. It is unclear whether this &lsquo;experiment&rsquo; will prove to be fruitious, but at this point I am willing to sacrifice of myself to find out.</p>

