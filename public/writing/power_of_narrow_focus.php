                    <p>This summer I am doing two things, and only two things&#8202;&mdash;&#8202;physics (as part of a research group at Harvard) and coding (as part of a start-up I&rsquo;m working on). I started the summer with the intention of doing many things: playing pool, playing tennis, learning to cook, hanging out with friends, learning complex analysis, writing a paper studying recursion in Kantian ethics, taking a statistics course online, painting a self-portrait, writing a collection of poems etc. I soon abandoned those pursuits, and now I do only two things.</p>

<p>I go to bed at 12:30am, I wake up at 6:30am, I start work on TimeStamp[1] at 7am. I get the bus to my physics lab at 10am and start work at 10:30am. We work through until 6pm, at which point I pack my bags, get the bus back, and work on TimeStamp until midnight with an hour for dinner at 8pm. [2]</p>

<p>I try to do this every weekday, in as regimented a fashion as possible. That is to say, this is *all* I do. There is no &lsquo;hanging out with friends&rsquo;, &lsquo;going for drinks after work&rsquo;, no &lsquo;chilling&rsquo;, and most importantly, there is no &lsquo;deciding what to do&rsquo;.</p>

<p>This is important.</p>

<p>When I finish work in my physics lab at 6pm, I have zero decisions to make. I don&rsquo;t need to choose between spending time with friends, going for a walk along the river, reading another chapter of Coetzee, or going for a run. I don&rsquo;t need to decide whether or not to shower, if I&rsquo;m hungry I know I won&rsquo;t eat until 8pm, I don&rsquo;t need to worry about which friends I haven&rsquo;t texted back, or which emails need answering. At 6pm, I walk from my physics lab to the office, I think about the TimeStamp tasks I have to do, I settle in to the work environment, and then I work&#8202;&mdash;&#8202;nowhere along this chain does a &ldquo;decision&rdquo; need to be made. That decision was made long ago when I decided to take the TimeStamp project on.</p>

<p>The most powerful thing about the dearth of decision making is that it frees up your mind to make real decisions. For a long time, I have been trying to eliminate the little voice in the back of my head which often seems a petulant child in need of continued attention. In religious teachings, the devil is said to be an adept &lsquo;whisperer&rsquo; but my internal voice sounds less like a whispering devil and more like &ldquo;the voice of reason&rdquo;. It is constantly trying to &ldquo;reason&rdquo; through every decision. Searching in vain for that last piece of information that will finally solve the puzzle and make this difficult decision trivially easy. Of course, such a puzzle-piece rarely exists, but that doesn&rsquo;t stop my internal monologue from scampering chaotically to explore and re-explore every possible nook and cranny. Even after a decision has been made, even after I have carefully considered the evidence, weighed the options and decided on the route I will take, my rational faculty still deems it worthwhile to re-examine old witnesses with regurgitated testimonies in search of a magic bullet. In traditional court cases, the case cannot be re-opened unless some substantial new piece of evidence has been brought to light. I sometimes wish my own internal monologue paid more respect to such jurisdiction.</p>

<p>The monotonic rhythm of this summer has been fantastic primarily in tackling this burden. I have been able to cocoon myself off from ordinary human society so effectively, that my mind is no longer rushing about frantically trying to satiate the whims and curiosities of those around me. I don&rsquo;t have people telling me, or showing me what I should be doing. I&rsquo;m forced instead to rely on my own instincts, make my own decisions about what I deem to be valuable, and what I deem to be superfluous. I am not fish-hooked by glamour, prestige, or precedent. I can take whichever path through the woods I so choose, and once a decision is made, the &lsquo;voice of reason&rsquo; is banished.</p>

<p>Immanuel Kant is said to have lived so regimented a life that you could tell time by his movements. I now understand why.</p>

<p>For once, that inner voice has been made quiet.</p>

