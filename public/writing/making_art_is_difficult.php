                    <p>I have tried. </p>

<p>In recent weeks I have been pursuing a young lady who is well versed in this kind of game. She is very capable of being just elusive enough to keep the chase interesting while also extending it to the brink of frustration. </p>

<p>I decided to take this bull by the horns, and demonstrate my intent. Of course, to do so overtly is usually a bad idea - openly admitting attraction is unbecoming, and we are prone to devalue the people we think are attracted to us too easily. Instead I chose to use art - specifically street art - as my medium of conversation. I wished to combine artistic nuance with elegance and subtlety to craft a set of experiences for this young woman that would enable her to transcend the mundanity of day-to-day life. I wasn't trying to 'win her over' for I know that affections are not easily persuaded, but rather trying to flex my creative muscles and show her what I was capable of. I wanted to give her an intense emotional experience.</p>

<p>I devised a 3 step plan: (1) Leave a message in chalk outside the door of a social club that she is a member of. A subtly crafted message that only she would understand [1]. All of her friends in the club would see this as they were walking in and out of the house. They would relay it to her: instead of me prodding for her attention, her inquisitive friends would serve as a constant reminder to her of me. It's leverage. I also think there's something beautiful in a publicly displayed cryptic message. The same wonder that we associate with skywriting. The note is there for everyone to see but only two people appreciate its true magnitude. Like eyes catching each other from across a crowded room.</p>

<p>[1] The message was actually &quot;To X, A sheep in wolf's clothing&quot;.</p>

<p>(2) Put up small 'messages' all over Harvard Yard. As she was walking from class to class she would continually be discovering small bright pieces of paper stuck to buildings all over campus. Each square of paper contained a small message [2]. The messages were varied - some playful, some arrogant, some accusatory, some philosophical, some soppy wet with romance, some just messages. Imagine walking around a familiar town and finding all your old friendly building presenting you with small gifts at every corner. Imagine being pleasantly surprised by discovery and rediscovery at every turn. It a treasure hunt. It takes a place you've been told is your home for so long and transforms into your very own canvas. Again, the element of surprise was important - as was the ideal that she would experience sporadic moments of magic even when entrenched in the mundane, routine act of moving between classes.</p>

<p>[2] &quot;X, What we call beauty is often a first intimation of terror&quot; was one of my favourites. As was: &quot;X, Today you should try telling a random stranger to go fuck themselves.&quot;</p>

<p>(3) Flowers on her desk. Taking things back to a more traditional form of romance - this would be the climax. I've long been in love with a particular illusion by Penn and Teller called &quot;Shadows&quot;. It's wonderful, it's simple. The scene begins with a set of flowers stood in a vase next to a tall white screen. A bright light projects a dark silhouetted shadow of the flowers onto the screen. Teller takes his knife and gently dabbles around the edges of the shadow which 'magically' causes the real life flowers to disintegrate as though they too could feel the sharp cuts that the shadow felt. It's aesthetically perfect. Even without the magic trick part of it, I think walking into a dark room with a bright desk lamp pointed at a bouquet of flowers casting a giant shadow on the wall is one of those 'magic moments' that you won't forget for a while. </p>

<p>However, my ideas and what the world has divined for me are not always the same thing. All three of these ideas collapsed miserably when confronted by the harsh light of reality. Reality, in many ways, sucks. </p>

<p>The chalk message had been removed by 11am the day it was put up. Only one person I know saw it. The message in a bottle was lost in the river and never reached it's destination. </p>

<p>Of the 64 pieces of coloured paper I put up around the yard, the vast majority were immediately removed by campus authorities. I finished putting up the last one at 6am, and when I walked to class at 10am, I found that only 5 of the most inventively concealed messages remained in their places.</p>

<p>I gave up on the flowers idea. It no longer seemed appropriate.</p>

