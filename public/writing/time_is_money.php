                    <p>I first think of &quot;Time is Money!&quot; as a phrase shouted by an overweight cigar smoker in a double-breasted blazer as he urges his army of monkey-typwriters to churn out more words per minute. He's clapping his hands together at waist height as he says this.</p>

<p>The phrase, however, means far more than &quot;Buckle up&quot;, &quot;Put your head down&quot;, or &quot;No rest for the weary&quot;. Today, I'd like to explore.</p>

<p>The similarities between time and money are perhaps best articulated in the words we use to speak about them. Both can be 'spent', 'misspent', 'invested', 'wasted', 'budgeted', 'set aside', 'allocated', 'borrowed', 'managed', 'shared'. We never seem to have enough of either, both can be exchanged for goods and services, your girlfriend probably wants more of both from you, and in many jobs there is a direct linear correspondence between hours worked and dollars earned.</p>

<p>However, there are two fundamental differences between time and money. The first is that time cannot be saved. The second is that you cannot 'make more' time. </p>

<p>Imagine for a moment that you were living on a fixed income - you make $4000 a month - such that at the end of every month your bank balance drops to zero. Any money you don't spend is taken by 'The Man' so he can light his cigars in style.</p>

<p>In such circumstances, how would you go about &quot;spending&quot; your money? </p>

<p>You would first cover the essentials, food, shelter, sleep, personal hygiene, family. Then you would buy things - you would buy exciting things, you would buy shiny things. On the 29th of the month you would rush to Best Buy and pick up all the gadgets you possibly could before your money evaporates at midnight. You would own lots of shoes. On the 30th of the month you will be eating either caviar or Custard Creams[1].</p>

<p>I go further however, and posit to you that the boom in consumerism that we have witnessed over the past 40 years is not merely a facet of our financial lives, but is also manifested in the way we spend our time. 'Consumerism' simply implies that our time is channelled towards short-term gratification and as a result we have no time left to spend on more meaningful activities. We waste all our time on tea kettles that can also boil rice and toothbrushes with built in computers, such that when that opportunity to fly over the Grand Canyon or row across the Atlantic comes along, we &quot;don't have the time&quot;. &quot;Not having time&quot; is sad. Unlike money, which is heavily misdistributed, you live with exactly the same amount of time as everyone else.</p>

<p>I would advise therefore, both to myself and to others, that we move away from shiny things. Use your time to purchase not individual moments of hedonism, but investments in yourself and others. Stockpile dried vegetables, buy lots of tins of tomato soup, buy real estate - square inches at a time, pour your money into your friends and their crazy art projects, spend your money in places where the product you purchase will be compounded rather than diminished over time. Use the excess money you have to buy bricks rather than breadmakers.</p>

<p>&quot;Consumerism&quot; has turned us in to a nation of magpies.</p>

<p>[1] For my American friends, 'Custard Creams' are a wonderful kind of British biscuit. They are also cheap.</p>

