                    <p>When I was 5 years old, I was a puppet master,<br />
These puppets, they sang and danced at the drop of a hat,<br />
Moving in ways I had never seen before,<br />
Each night they would tap it, rock it and pop it,<br />
Each and every night with the same enthusiasm<br />
It was as though they just couldn't stop smiling,<br />
Their dancing was effortless, it was elegance,<br />
Though my fingers played the notes, the song was all theirs,<br />
We had dreams of performing at institutions like this,<br />
Introducing those over-privileged, book-smart street-dumb students,<br />
Those elitist twits, to the magic they missed,<br />
Teach them a new perspective, show them how my world looks,<br />
Get them to stop reading books as though they were people, and stop reading people like books,</p>

<p>Till one day I was asked,<br />
When your talents take you far and you become a star,<br />
What will become of us?<br />
And I made promises of loyalty I never knew could be broken,<br />
Told them that I would never let go,<br />
They would forever hang,</p>

<p>When Harvard sent me that letter, I barely said goodbye,<br />
I didn't have the time,<br />
And I tried to pull all my friends all up with me, but you got too high<br />
and the magic was lost,<br />
So instead I had to let those strings hang and required you to climb,</p>

<p>When I go back home and they say to me:<br />
We can't count high enough to how far away you've flown,<br />
And what of those of us who are still at home, we are on our own,<br />
Living 'real' life as you sit in your academic throne,<br />
I tell these puppets, you were never abandoned, you were just outgrown,<br />
So many &quot;opportunities&quot;<br />
Thrust in your face you can barely breathe,<br />
It was we who raised you, we who trained you,<br />
And yet you have the nerve to assert that you deserve this,<br />
That it was you who earned this,<br />
Oh you may be smart but you don't own this Earth, kid.</p>

<p>Only after the strings have been severed I realize,<br />
As they lie limp, a clump of mangled wood, able to dance no more,<br />
That their perfectly carved forms,<br />
Spoiled only by these jagged wiry strings, that forced their way inside them, <br />
They have every right to curse the stigmata,<br />
Holes in their hands, and heavy wooden crosses they hung from,<br />
The straightness of these ungodly strings,<br />
They tarnish the beautiful smooth curved wood grain veins that map out the inner circuitry of their beings,<br />
I try to retort,<br />
Though I may be the one pulling the strings,<br />
They are useless unless they are torturously taught,<br />
I will make sure that they, are looking only at you.</p>
