                    <p>
She is one of life's unfortunate ones. </p>

<p>She is grotesque. Unloved. And undeserving of love. </p>

<p>It is her body language, and her body. The grotesqueness sits behind the eyes of an otherwise warm smile. It is the stuttering laugh, and the effort she puts in to every movement. It is the shape her spine makes as she slouches a gentle slouch in her chair. A slouch that - had it been on the body of someone less grotesque - would not have been quite so objectionable. </p>

<p>The grotesqueness permeates, and touches like a horrid Midas, everything it breathes on. It makes the otherwise innocuous mole on her neck into a displeasing deformity. It turns her eyebrows square and her breath salty. It makes her breathing loud an noticeable. It takes her potentially beautiful hands and slumps them lifelessly on the table - lifeless, and flat and soft.</p>

<p>She might raise her hand to pass the pepper and unknowingly send a grotesque wriggle through the fat that dangles on the outside of her arm. She will stand gracelessly, and walk across the room with clumpy feet and droop.</p>

<p>Sometimes she tries to hide the grotesqueness, with a kind of self-esteem face powder. She will sit up straight, and twinkle her eyes, and feign disinterest, but this act is all too transparent, and its transparency is projected all over the room like perfume. Sometimes we try to hide her grotesqueness for her. The act does not last for it never can. </p>

<p>The grotesqueness is not a property she &quot;possesses&quot;, for possession implies it can be bought or sold or traded. The grotesqueness just is, it is the prerequisite, it is background, it is paper, it is stage.</p>

<p>It is not a grotesqueness that can identified, isolated and surgically removed. She is not even aware of her own grotesqueness sometimes, and yet it is ever present, like the sound of chewing. Mastication. Or long swallowing. It is a grinding that background-noises all she says and all she is. </p>

<p>Sometimes, others do not perceive it in her, and treat her as though she is ordinary. Sadness does not come cascading into her like waterfalls, It is small droplets condensing in foggy window frames.</p>
